// Cluster map pins from activities
export interface MapCluster {
  id: string;
  x: number;
  y: number;
  count: number;
  activityIds: string[];
  isMulti: boolean; // true if activities share exact same GPS coordinates
}

export function clusterActivities(activities: Activity[], gridSize: number = 12): MapCluster[] {
  const clusters: MapCluster[] = [];
  const assigned = new Set<string>();

  for (const activity of activities) {
    if (assigned.has(activity.id)) continue;

    const cluster: MapCluster = {
      id: `cluster-${activity.id}`,
      x: activity.mapX,
      y: activity.mapY,
      count: 1,
      activityIds: [activity.id],
      isMulti: false,
    };

    // First pass: find exact GPS matches (multi-activity)
    const exactMatches: Activity[] = [];
    for (const other of activities) {
      if (other.id === activity.id || assigned.has(other.id)) continue;
      if (activity.mapX === other.mapX && activity.mapY === other.mapY) {
        exactMatches.push(other);
        cluster.count++;
        cluster.activityIds.push(other.id);
        assigned.add(other.id);
      }
    }

    if (exactMatches.length > 0) {
      cluster.isMulti = true;
    } else {
      // Second pass: proximity clustering (only if no exact matches)
      for (const other of activities) {
        if (other.id === activity.id || assigned.has(other.id)) continue;
        const dx = Math.abs(activity.mapX - other.mapX);
        const dy = Math.abs(activity.mapY - other.mapY);
        if (dx < gridSize && dy < gridSize) {
          cluster.count++;
          cluster.activityIds.push(other.id);
          cluster.x = (cluster.x + other.mapX) / 2;
          cluster.y = (cluster.y + other.mapY) / 2;
          assigned.add(other.id);
        }
      }
    }

    assigned.add(activity.id);
    clusters.push(cluster);
  }

  return clusters;
}

